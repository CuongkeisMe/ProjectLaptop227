    private void showDataTableSP(ArrayList<main.entity.BanHang> list) {
        dtmSP.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            main.entity.BanHang bh = list.get(i);
            dtmSP.addRow(new Object[]{
                bh.getMaSP(),
                bh.getTenSP(),
                bh.getTenCPU(),
                bh.getTenGPU(),
                bh.getTenRam(),
                bh.getTenMH(),
                bh.getTenOCung(),
                bh.getPin(),
                bh.getGiaSP(),
                bh.getSoLuongSP()
            });
        }
    }

